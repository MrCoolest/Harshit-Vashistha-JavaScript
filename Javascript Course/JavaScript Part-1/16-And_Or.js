// And or Operator

let firstname = "Ankit";
let age = 20;

// and operator &&
if(firstname[0] === 'A' && age > 18 ){
     console.log("name starts with A and age is above 18");
}


// or operator ||
if(firstname[0] === 'A' || age > 24 ){
     console.log("name starts with A and age is above 18");
}

